class ConfigBuilder:
    """Builds O11V4 configuration from Kayo data."""
    
    @staticmethod
    def build_channel(name: str, channel_id: str, mode: str = "live",
                     **kwargs) -> Dict[str, Any]:
        """Build O11V4 channel configuration."""
        return {
            "Name": name,
            "Mode": mode,
            "SessionManifest": True,
            "ScriptParams": f"id={channel_id}",
            "OnDemand": kwargs.get("on_demand", False),
            "SpeedUp": kwargs.get("speed_up", True),
            "Autostart": kwargs.get("autostart", False),
            "Video": kwargs.get("video", "best"),
            "Audio": kwargs.get("audio", "all"),
            "CdmType": kwargs.get("cdm_type", "widevine") if kwargs.get("drm") else None,
            "UseCdm": kwargs.get("use_cdm", False)
        }
    
    @staticmethod
    def build_event(title: str, event_id: str, start_timestamp: int,
                   end_timestamp: int, **kwargs) -> Dict[str, Any]:
        """Build O11V4 event configuration."""
        return {
            "Name": title,
            "Mode": kwargs.get("mode", "vod"),
            "SessionManifest": True,
            "ScriptParams": f"id={event_id}",
            "OnDemand": kwargs.get("on_demand", False),
            "Autostart": kwargs.get("autostart", False),
            "Start": start_timestamp,
            "End": end_timestamp,
            "RecordEvent": kwargs.get("record_event", False),
            "Video": kwargs.get("video", "best"),
            "Audio": kwargs.get("audio", "all")
        }
    
    @staticmethod
    def build_config(provider_name: str = "kayo", channels: List[Dict] = None,
                    events: List[Dict] = None) -> Dict[str, Any]:
        """Build complete O11V4 configuration."""
